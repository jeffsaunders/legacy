				<p style="position:relative;">Costa Rica is situated on the Central American isthmus, flanked by the Pacific Ocean and Caribbean Sea with a distance of approximately 248 kilometers (155 miles) between coasts. With a total area of 51,200 square kilometers (20,480 square miles), Costa Rica is no larger than the state of West Virginia, yet it contains 6% of the world's biodiversity. Costa Rica is well known for the premium it places on peace, education, democracy and nature. In 1949, the government abolished the army, allocating all would-be military expenses to education and health care. As a result, the literacy rate rose to 95% and continues to be one of the highest rates in the world.</p>

				<p style="position:relative;">International ports on both coasts; air, sea and freight transportation services; a well-developed infrastructure and a strategic location at the crossroads of two continents make Costa Rica a contender in world markets. The government's receptiveness towards new business ventures and excellent incentive plans have lured a growing number of multi-national corporations to the country.</p>


