				<p><img src="images/spacer.gif" alt="" width="2" height="5" border="0"><br>
				Creating a place in Las Vegas that would offer help and employment for blind people was F. Marion Keele's dream since 1934 when he became totally blind at age 38. After spending time at the Utah Blind Center in Salt Lake City with his wife, Effie, Marion learned how to teach Braille, rug weaving and braiding, leather and other handicrafts. In addition to bringing this valuable information to other blind people living in Las Vegas, he lobbied State legislators in Carson City to increase state aid to the blind which, at that time, was $20 per month. The <strong>Nevada Association of the Blind</strong>, later called the Southern Chapter, held its first meetings in the Keele's home.</p>

				<p>Upon Marion's death in 1955, one of his students, Audrey Bascom Tait, continued his work and on February 5, 1955 she filed Articles of Incorporation for <strong>Southern Nevada Sightless</strong>.  As time went on Audrey convinced the Lion's Club to help her find a place where chair caning and the looms for weaving rugs could be set up permanently, providing work opportunities for the blind. On December 18, 1960 with the assistance from many Lion's Clubs, a two-room building at 1001 Bruce Street and that location has been the Center's home ever since. On April 16, 2002, the facility was renamed the <strong>Blind Center of Nevada</strong>.</p>

				<p>In keeping pace with the changing needs of the community, the <strong>Center</strong> began a revitalization program that began with a new Board of Directors in May of 2000.  The challenge of the Board of Directors is to lay the groundwork to bring hope to those who are blind or visually impaired.  To meet the challenge the <strong>Center</strong> focuses on expanding public awareness, community involvement and job development to create meaningful employment to improve the quality of life for our members.</p> 

