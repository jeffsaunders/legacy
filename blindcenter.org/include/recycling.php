				<p><strong>Is a Computer an Asset or a Liability?</strong></p>

				<p>When it comes to the computer's end-of-life, the answer is easy: <strong>liability!</strong></p>

				<p>A computer contains information. Exposing that information can cause damage to a variety of agents. Furthermore, that information is protected by law. The process of clearing information from the hard drive, beyond recovery, is called: <strong>"Hard Drive Sanitization"</strong>. Evidence that the hard drive was sanitized is called: <strong>"Certificate of Destruction"</strong>.</p>

				<strong>Motivations for Hard Drive Sanitization</strong>
				<ul>
					<li>Losing Customers' Trust, Reputation</li>
					<li>Security and Privacy</li>
					<li>Risk Management</li>
					<li>Acquiring a Certification of Data Destruction</li>
					<li>Disposing oversized storages of your old electronics</li>
					<li>Environmental factors</li>
				</ul>

				<p><strong>Compliance</strong></p>
				<p>Laws and regulations make organizations responsible for their employees' and customers' information, and require proper disposal of the hard drives. Laws include: <strong>HIPPA, FACTA, SBA, GLBA, PIPEDA, SEC Rule 17a, Californa Senate Bill 1386</strong>.</p>

				<p><strong>The Challenges</strong></p>
				<p>Federal agencies such as DoD, DSS and NIST set standards on how to protect and sanitize electronic information. Yet, developing and implementing protocols in your organization easily becomes a sophisticated task. <strong>Robert Varmosi</strong>, the senior editor of <strong>CNET Review</strong> wrote: <em>"the process is mind-numbing and subject to human error… There is no way to easily visually check that erasure has successfully occurred"</em>.</p>

