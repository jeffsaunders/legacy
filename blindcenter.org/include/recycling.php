				<p>Currently, the useful life of a computer is only 3 to 5 years and shrinking. In 2005, the National Safety Council projects more than 63 million personal computers are expected to retire.</p>

				<p>If you have old, outdated electronic products (e.g., personal computers, peripherals, laptops, monitors, fax machines and copiers) in your office or home, you're not alone. According to the Institute for Local Self-Reliance, approximately 75 percent of obsolete electronics are currently being stored or warehoused without any idea on the best way to handle these materials.</p>

				<p>Electronic products can contain over a dozen hazardous or toxic materials. The cathode ray tube (CRTs) in monitors can be up to 27 percent lead, and in a form that can leach into the water table if dumped in a landfill. Other chemicals such as cadmium and lithium (in rechargeable batteries), mercury (in switches and lamps) as well as chromium and antimony can be found in amounts that may cause them to test hazardous under Federal law.</p>

				<p>Many electronic products contain parts that could be profitably refurbished and reused with little effort. When we throw away old electronic equipment, we're throwing away these resources and generating additional pollution associated with the need to access virgin materials and manufacture new products.</p>

				<p>Technological advances are rapidly rendering formerly cutting-edge electronics obsolete. An estimated 20 million personal computers became obsolete in 1988. Most of these are in storage. Of the remainder less than 6 percent was recycled.</p>

