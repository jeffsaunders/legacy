		<p align="left"><font size="3"><strong>What is Podiatry?</strong></font></p>
		
		<p align="left">Podiatric medicine focuses on diagnosing, treating, and preventing foot and ankle problems such as <a href="?sec=info&ailment=plantar fasciitis" class="bodyBlue">Plantar Fasciitis</a> (chronic heel pain syndrome).  Your podiatric shysician (podiatrist) is highly trained through years of study and clinical practice, including a bachelor's degree program and four years of podiatric medical school.  In addition, podiatrists often have one or more years of residency training to study foot care and surgery in greater depth.  Once in practice, posiatrists continue to stay informed about the latest research and developments in the field.</p>

		<p align="left">In treating your feet, your podiatrist may also work with your other health care providers to give you appropriate and comprehensive care.</p>

