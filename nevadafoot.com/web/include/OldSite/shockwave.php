		<p align="left"><br><font size="3"><strong>How Does ESWT Work?</strong></font></p>
		
		<p align="left">Shockwave therapy works by triggering "messengers" that activate the body's own repair mechanisms to increase blood flow, change cell permeability, and enhance tendon healing.  Shockwaves also over-stimulate the nerves that may block the transmission of pain and lead to a reduction in sensitivity.

		<p align="left"><font size="3"><strong>Before Your Treatment</strong></font></p>
		
		<p align="left">Prior to having ESWT, you will be asked for a list of all medications that you currently take.  It is important to include <strong>all</strong> medications, not only prescriptions.  Please list any over-the-counter medications such as aspirin, vitamins, or herbal remedies.</p>

		<p align="left">For safetly reason you are required to have someone drive you home from the treatment center, so please make arrangements prior to your treatment day.</p>

		<p align="left">Your physician may give you a prescription to take prior to your treatment.  Follow the instructions as prescribed.</p>

