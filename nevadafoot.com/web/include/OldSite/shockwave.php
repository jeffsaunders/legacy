		<p align="left">During the treatment, the shockwave therapy head is placed in contact with the afflicted area.  A gel is used to ensure the therapeutic shockwaves are properly transmitted to the location of the pain.  Your physician targets the therapeutic waves via ultrasound imaging to achieve the optimal results.  The entire treatment process takes approximately 45 minutes.</p>

		<p align="left">Soon after your treatment you will be released and may be driven home by your designated driver.  After a short resting period and adhering to your physician's directions, you may return to normal activities.  Your physician will explain to you what you can expect after your treatment and you will be scheduled for a follow-up evaluation.</p>

		<p align="left"><font size="3"><strong>Insurance Coverage</strong></font></p>
		
		<p align="left">Extracorporeal Shockwave Therapy, ESWT, is considered an alternative therapy to surgical intervention.  To see how your insurance plan covers ESWT, ask your insurance provider if this is a covered benefit.  AKSM/Ortho may be an out-of-network service provider for this procedure.  An out-of-network provider may increase your financial responsibility for the treatment.

