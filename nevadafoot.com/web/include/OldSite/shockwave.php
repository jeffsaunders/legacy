		<p align="left"><font size="3"><strong>Shockwave Therapy</strong></font></p>
		
		<p align="left">Shockwave therapy is best known for its use in the treatment of kidney stones, referred to as Extracorporeal Shockwave Lithotripsy (ESWL).  Research has shown that shockwave therapy applied appropriately is also beneficial for the treatment of several orthopedic and podiatric conditions &mdash; this procedure is called <strong>Extracorporeal Shockwave Therapy</strong>.</p>

		<p align="left">Nevada Foot Institute offers Extracorporeal Shockwave Therapy (ESWT).  This non-invasive procedure is performed on an outpatient basis &mdash; it does not require open surgery or a hospital stay.  Patients may leave the treatment center shortly after the procedure is complete.</p>

		<p align="left">In October 2000, the FDA granted clearance of ESWT for the treatment of <a href="?sec=info&ailment=plantar fasciitis" class="bodyBlue">Plantar Fasciitis</a> (chronic heel pain syndrome) and in July 2002 granted clearance for the treatment of <a href="?sec=info&ailment=epicondylitis" class="bodyBlue">Epicondylitis</a>.  Additional treatment indications are currently being researched.</p>
