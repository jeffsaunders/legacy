		<p align="left"><font size="3"><strong>Treatment Day</strong></font></p>
		
		<p align="left">Arrive at the treatment center one hour prior to the procedure.  Wear loose-fitting, comfortable clothing and shoes.  Once you arrive, you will be taken to the pre-op area for your treatment preparations.  If you are scheduled for local anesthesia, you will reccive it at that time.</p>

