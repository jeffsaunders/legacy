				<div align="right"><p style="position:relative;"><font size="5"><strong>Frequently Asked Questions</strong></font></p></div>

				<p style="position:relative;">Welcome to the <strong>R Toe Rings</strong> F.A.Q. Hopefully you will find answers to all your questions here however, if you don't, please <a href="?sec=contact" title="Email Us" class="bodyBlue" style="position:relative;"><strong>e-mail</strong></a> them to us and we'll do our best to get them answered as soon as possible.</p><br>

				<p style="position:relative;"><strong>Q: Are toe rings comfortable?</strong><br><br>
				<strong>A:</strong> Absolutely. A good fitting toe ring will never bother you. You may even forget you're wearing it, just like a finger ring. The placement of the toe ring is important. The ultimate comfort zone is in the middle of the toe, just after the pad at the top of the toe and before the knuckle.</p><br>

				<p style="position:relative;"><strong>Q: Can I wear a toe ring with shoes?</strong><br><br>
				<strong>A:</strong> Yes. Toe rings can be worn in any circumstance and in almost all shoes, except very tight shoes (ballerina shoes, for example). As the toe ring has become more mainstream, we've seen them worn from beach to boardroom and everywhere in between.</p><br>

				<p style="position:relative;"><strong>Q: What is the difference between a toe ring and a finger ring?</strong><br><br>
				<strong>A:</strong> A toe ring, properly designed, is very sleek and contoured. At RToe Rings, we have custom fit thousands of toes, and have learned that bulky finger ring styles are not comfortable on the toe.</p><br>

				<p style="position:relative;"><strong>Q: Do toe rings bend easily?</strong><br><br>
				<strong>A:</strong> Only if the toe ring is clearly too large will it bend easily. If it is properly fit and worn on the middle of the toe - where it is not likely to touch the ground - it should never bend.</p><br>

				<p style="position:relative;"><strong>Q: Will I lose my toe ring?</strong><br><br>
				<strong>A:</strong> Most people have a fairly large pad under their toe and can wear a toe ring 24/7 without fear of it slipping off. However, if you don't have a pad at the top of your toe, you should take it off when swimming or bathing.</p><br> 

				<p style="position:relative;"><strong>Q: How do I get my toe ring on?</strong><br><br>
				<strong>A:</strong> We use a quick spray of Windex to lubricate the toe so that the toe ring slides on easily. Any kind of soapy water will do.</p><br>

<p style="position:relative;"><strong>Q: How can I tell if I have a perfect fit?</strong><br>
<ul><li>You should not be able to get your toe ring on or off without Windex.</li>
<li>There should be approximately 1/4 size play between your skin and the ring. If you can't spin your toe ring, it is too tight.</li> 
<li>After wearing your toe ring for a day or so, you should feel like nothing is there.</li></ul></p><br> 

				<p style="position:relative;"><strong>Q: What toe do people generally wear a toe ring on?</strong><br><br>
				<strong>A:</strong> Either the right foot or the left - there is no symbolism. Generally, the second and the third toes are popular because they are the longest and most comfortable.</p><br>

				<p style="position:relative;"><strong>Q: What if my toe ring doesn't fit right?</strong><br><br>
				<strong>A:</strong> Simply return it to us in 30 days for a different size, or for a full refund.<sup>*</sup><p> 

